print( "The second number is: " , numTwo, "\n" )
print( "The numbers sum is: ", numOne + numTwo )
print( "The numbers subtraction is: ", numOne - numTwo )
print( "The numbers multiplication is: ", numOne * numTwo )
print( "The numbers division is: ", numOne / numTwo )
