# Control Structures: If, Elif, Else
birth_place = "MX"
age = 17
if birth_place == "MX" and age >= 18:
    print( "Puede Votar" )
else:
    print( "No Puede Votar" )
# Validar si la persona es de MX y es mayor de edad Cumple "Puede Votar", No cumple "No puede votar"

# Control Structures: For
students = [ "Claudia", "Derek", "Leonel", "Mónica", "Ángélica", "Jaime", "Gaby"  ]
for x in range( len( students ) ):
    print( students[ x ] )

