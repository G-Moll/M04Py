# Dictionaries
# Los diccionarios pueden contener ditintos tipos de datos, tanto primitivos como complejos
# Cada elemento un diccionario está formado por una llave (key) y un valor (value)
# Para accerder a las llaves de un diccionario, se usa una sintáxis como la siguiente
some_dict = { "name": "Python", "ver": 3.12 }
print( some_dict )
print( some_dict[ "name" ] )
print( some_dict[ "ver" ] )
